/**
 * 成员变量和局部变量的区别
 *  1. 在类中的位置不同
 *      成员变量: 在类中,方法体外面
 *      局部变量: 方法中或方法声明上(形参)
 *  2. 在内存中的位置不同
 *      成员变量: 堆内存
 *      局部变量: 栈内存
 *  3. 生命周期不同
 *      成员变量: 随着对象的创建而存在,随着对象的消失而消失
 *      局部变量: 随着方法的调用而存在,随着方法的调用完成而消失
 *  4. 初始化值的
 *      成员变量: 有默认值
 *      局部变量: 没有默认值,必须先定义,赋值,然后再使用
 *
 */
